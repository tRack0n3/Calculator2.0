package Calculator;


class ArithmeticOperation {

    static int subtract(int num1, int num2) {
        return num1 - num2;
    }

    static int addition(int num1, int num2) {
        return num1 + num2;
    }

    static int multiply(int num1, int num2) {
        return num1 * num2;
    }
    static int divine (int num1, int num2){
        return num1 / num2;
    }
}
